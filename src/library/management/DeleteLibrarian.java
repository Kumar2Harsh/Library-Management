    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try{
            Connection con=Db.getConnection();
            PreparedStatement ps = con.prepareCall("delete from librarian where id=?");
            ps.setInt(1, Integer.parseInt(librarianId.getText()));
            int status=ps.executeUpdate();
            if(status != 0)
            {
                JOptionPane.showMessageDialog(null, "Record Sucessfully Deleted", null, JOptionPane.OK_OPTION);
                librarianId.setText("");
                
            }
            else
                JOptionPane.showMessageDialog(null, "Deletetion fail !!!", null, JOptionPane.OK_OPTION);
        }catch(Exception e){}
    }//GEN-LAST:event_deleteActionPerformed

