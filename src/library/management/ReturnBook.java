  public void updateIssuedBook()
  {
      Connection con = Db.getConnection();
      int status = 0;
       getQuentityAndIssued();
         try{
            PreparedStatement ps = con.prepareStatement("update books set quantity=?,issued=? where callno=?");
            ps.setInt(1, quantity+1);
            ps.setInt(2, issued-1);
            ps.setString(3, bookCallNo.getText());
            status = ps.executeUpdate();
            if(status==0)
                 JOptionPane.showMessageDialog(null, "No Book issued", "Warning", JOptionPane.OK_OPTION);
          }catch(Exception e){
              System.out.println(e.toString());
          }
   }
public void getQuentityAndIssued()
  {
      
      Connection con = Db.getConnection();
      try{
             PreparedStatement ps = con.prepareStatement("select quantity,issued from books where callno=?");
             ps.setInt(1, Integer.parseInt(bookCallNo.getText()));
             ResultSet rs=ps.executeQuery();
             if(rs.next())
             {
              quantity = rs.getInt("quantity");
              issued = rs.getInt("issued");
             }
             else
                 JOptionPane.showMessageDialog(null, "Invalid Call No. !!", null, JOptionPane.OK_OPTION, null);
      }catch(Exception e)
      {
          System.out.println(e.toString());
      }
     
  }
